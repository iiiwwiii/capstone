package picture;

public class AlbumVO {
	private int album_num;
	private String album_title;
	private String album_content;
	private String album_title_image;
	
	public int getAlbum_num() {
		return album_num;
	}
	public void setAlbum_num(int album_num) {
		this.album_num = album_num;
	}
	public String getAlbum_title() {
		return album_title;
	}
	public void setAlbum_title(String album_title) {
		this.album_title = album_title;
	}
	public String getAlbum_content() {
		return album_content;
	}
	public void setAlbum_content(String album_content) {
		this.album_content = album_content;
	}
	public String getAlbum_title_image() {
		return album_title_image;
	}
	public void setAlbum_title_image(String album_title_image) {
		this.album_title_image = album_title_image;
	}
}
